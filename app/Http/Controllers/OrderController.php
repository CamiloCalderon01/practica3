     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $orders = Order::select(
            "orders.ord_num",
            DB::raw("ROUND(orders.ord_amount, 0) as ord_amount"),
            "orders.advance_amount",
            "orders.ord_date",
            "customers.cust_name",
            "customers.cust_city",
            DB::raw("CONCAT(agents.agent_code, ' - ', agents.agent_name) as agent_info")
        )
        ->join("agents", "fk_agent_code", "agent_code")
        ->join("customers", "fk_cust_code", "cust_code")
        ->paginate(1);
        return response()->json($orders);
    }

